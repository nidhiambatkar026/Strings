public class compareTo {
    public static void main(String args[]){
        String name1 = "Nidhi";
        String name2 = "nidhi";

        if(name1.compareTo(name2) == 0){
              System.out.println("Strings are equal");
        }
        else{
            System.out.println("Strings are not equal");
        }
    }
}
